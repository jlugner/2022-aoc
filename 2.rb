PLAYS = {
  rock: { score: 1, beats: :scissor, lose: :paper },
  paper: { score: 2, beats: :rock, lose: :scissor },
  scissor: { score: 3, beats: :paper, lose: :rock }
}.freeze

def get_play(key)
  case key
  when 'A', 'X' then :rock
  when 'B', 'Y' then :paper
  when 'C', 'Z' then :scissor
  end
end

def play_round(move1, move2)
  if move1 == move2
    3
  elsif PLAYS.dig(move1, :beats) == move2
    6
  elsif PLAYS.dig(move1, :lose) == move2
    0
  else
    raise "Unable to play round between #{move1} and #{move2}"
  end
end

rounds = DATA.readlines.map(&:chomp)

# A
score = rounds.inject(0) do |sum, moves|
  opponent_move, your_move = moves.split(' ').map { get_play(_1) }
  sum + PLAYS.dig(your_move, :score) + play_round(your_move, opponent_move)
end
p "a: #{score}"

# B
score = rounds.inject(0) do |sum, moves|
  opponent_move, strategy = moves.split(' ')
  opponent_move = get_play(opponent_move)
  your_move = case strategy
              when 'X' then PLAYS.dig(opponent_move, :beats) # lose to them
              when 'Y' then opponent_move                    # play draw
              when 'Z' then PLAYS.dig(opponent_move, :lose)  # beat them
              end
  sum + PLAYS.dig(your_move, :score) + play_round(your_move, opponent_move)
end
p "b: #{score}"

